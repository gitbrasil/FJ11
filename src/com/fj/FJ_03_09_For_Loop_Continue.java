package com.fj;

public class FJ_03_09_For_Loop_Continue {

    public static void main(String[] args) {
        // 
        for (int i = 0; i < 100; i++) {
            if (i >= 50 && i <= 60) {
                System.out.println("Num. entre 50 e 60: " + i);
            }
        }

    }

}
